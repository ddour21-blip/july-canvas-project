/** 프로젝트 모드에 따라 프로토타입 제작 패키지(markdown)를 생성. */
export const buildPrototypePackage = (project: Project, a: ProjectActivation): string =>
  resolveProjectMode(a) === 'requirement_planning'
    ? buildRequirementPackage(project, a)
    : buildIdeaPackage(project, a);
